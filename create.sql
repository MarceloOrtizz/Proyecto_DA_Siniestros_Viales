DROP TABLE IF EXISTS `HECHOS`;

CREATE TABLE Hechos (
    ID VARCHAR(10) PRIMARY KEY,
    N_VICTIMAS INT,
    FECHA DATE,
    AAAA INT,
    SEMESTRE INT,
    ANIO_SEMESTRE VARCHAR(10),
    MM INT,
    DD INT,
    HORA TIME,
    HH INT,
    DIA VARCHAR(10),
    LUGAR_DEL_HECHO VARCHAR(255),
    TIPO_DE_CALLE VARCHAR(10),
    CALLE VARCHAR(255),
    CRUCE VARCHAR(255),
    DIRECCION_NORMALIZADA VARCHAR(255),
    COMUNA INT,
    XY_CABA VARCHAR(50),
    LONGITUD FLOAT,
    LATITUD FLOAT,
    PARTICIPANTES VARCHAR(50),
    VICTIMA VARCHAR(50),
    ACUSADO VARCHAR(50)
)ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

SET GLOBAL local_infile = ON;

LOAD DATA local INFILE 'C:/ProgramData/MySQL/MySQL Server 8.0/Uploads/homicidios_hechos.csv' INTO TABLE HECHOS
